package com.example.project1.model;

public class Kaiwa {
    private int kaiwa_id, lesson_id;
    private String character, kaiwa, mean;

    public int getKaiwa_id() {
        return kaiwa_id;
    }

    public void setKaiwa_id(int kaiwa_id) {
        this.kaiwa_id = kaiwa_id;
    }

    public int getLesson_id() {
        return lesson_id;
    }

    public void setLesson_id(int lesson_id) {
        this.lesson_id = lesson_id;
    }

    public String getCharacter() {
        return character;
    }

    public void setCharacter(String character) {
        this.character = character;
    }

    public String getKaiwa() {
        return kaiwa;
    }

    public void setKaiwa(String kaiwa) {
        this.kaiwa = kaiwa;
    }

    public String getMean() {
        return mean;
    }

    public void setMean(String mean) {
        this.mean = mean;
    }
}
